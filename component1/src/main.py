"""
This is a collection of mathematical functions, including:
- add(x, y) which returns the sum of x and y
- subtract(x, y) which returns the difference of x and y
- multiply(x, y) which returns the product of x and y
- divide(x, y) which returns the result of x divided by y
"""

def add(x: int, y: int) -> int:
    """
    This function takes two integers as input, x and y, and returns their sum.
    """
    return x + y


def subtract(x: int, y: int) -> int:
    """
    This function takes two integers as input, x and y, and returns their difference.
    """
    return x - y


def multiply(x: int, y: int) -> int:
    """
    This function takes two integers as input, x and y, and returns their product.
    """
    return x * y


def divide(x: int, y: int) -> int:
    """
    This function takes two integers as input, x and y, and returns the result of x divided by y.
    """
    return x / y
